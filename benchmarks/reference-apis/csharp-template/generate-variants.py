#!/usr/bin/env python3
"""Regenerate the nine C# reference-API variants from the single template.

API-SPEC.md §8: one template, version-gated deltas only — "a change to the
API must be impossible to make in one variant only".

    ./generate-variants.py          # rewrite csharp-net6 … csharp-net10-aot + csharp-net48
    ./generate-variants.py --check  # verify the committed variants match the
                                    # template output (exit 1 on drift)

Sources of truth (edit these, never the variant copies):

    csharp-template/Program.cs   all eight modern variants (byte-identical
                                 copies; TFM deltas are #if NET*_OR_GREATER)
    csharp-template/Server.cs    csharp-net48 (HttpListener, C# 5, Windows)
    this script                  csproj / Dockerfile / build.sh / deploy.sh /
                                 README.md / .gitignore per variant
"""
import os
import shutil
import stat
import sys
import tempfile

SCRIPT_DIR = os.path.dirname(os.path.abspath(__file__))
REF_DIR = os.path.dirname(SCRIPT_DIR)

BANNER = ("GENERATED by csharp-template/generate-variants.py — DO NOT EDIT "
          "(edit the template, then regenerate)")

# (variant, tfm, sdk_image_tag, aot)
MODERN = [
    ("csharp-net6", "net6.0", "6.0", False),
    ("csharp-net7", "net7.0", "7.0", False),
    ("csharp-net8", "net8.0", "8.0", False),
    ("csharp-net8-aot", "net8.0", "8.0", True),
    ("csharp-net9", "net9.0", "9.0", False),
    ("csharp-net9-aot", "net9.0", "9.0", True),
    ("csharp-net10", "net10.0", "10.0", False),
    ("csharp-net10-aot", "net10.0", "10.0", True),
]

ALL_VARIANTS = [v[0] for v in MODERN] + ["csharp-net48"]


def csproj(variant, tfm, aot):
    aot_block = "    <PublishAot>true</PublishAot>\n" if aot else ""
    return f"""<!-- {BANNER} -->
<Project Sdk="Microsoft.NET.Sdk.Web">

  <PropertyGroup>
    <TargetFramework>{tfm}</TargetFramework>
    <!-- /health "runtime" identity (API-SPEC.md §5.1) -->
    <AssemblyName>{variant}</AssemblyName>
    <RootNamespace>BenchReference</RootNamespace>
    <LangVersion>latest</LangVersion>
    <Nullable>enable</Nullable>
    <ImplicitUsings>enable</ImplicitUsings>
    <InvariantGlobalization>true</InvariantGlobalization>
{aot_block}  </PropertyGroup>

</Project>
"""


def dockerfile(variant, sdk, aot):
    aot_tools = """\
# Native AOT needs clang + zlib headers; no cross-compilation, so the build
# runs on the target architecture.
RUN apt-get update && apt-get install -y --no-install-recommends \\
    clang zlib1g-dev && \\
    rm -rf /var/lib/apt/lists/*
""" if aot else ""
    self_contained = "" if aot else " --self-contained"
    return f"""# {BANNER}
# --- Build stage ---
FROM mcr.microsoft.com/dotnet/sdk:{sdk} AS build
{aot_tools}ARG TARGETARCH
WORKDIR /src
COPY {variant}.csproj Program.cs ./
RUN case "$TARGETARCH" in arm64) rid=linux-arm64 ;; *) rid=linux-x64 ;; esac && \\
    dotnet publish -c Release -r "$rid"{self_contained} -o /app/out {variant}.csproj

# --- Runtime stage --- (self-contained/native output: runtime-deps suffices)
FROM mcr.microsoft.com/dotnet/runtime-deps:{sdk}
WORKDIR /app
COPY --from=build /app/out .

ENV BENCH_CERT_DIR=/opt/bench
ENV BENCH_PORT=8443
EXPOSE 8443

ENTRYPOINT ["./{variant}"]
"""


def build_sh(variant, aot):
    kind = ("Native AOT publish for linux-x64 (requires clang on the build host)."
            if aot else "Self-contained publish for linux-x64.")
    return f"""#!/usr/bin/env bash
# {BANNER}
# {kind}
set -euo pipefail

SCRIPT_DIR="$(cd "$(dirname "$0")" && pwd)"
cd "$SCRIPT_DIR"

dotnet publish -c Release -r linux-x64 -o ./publish

echo "Build complete. Binary: ./publish/{variant}"
ls -lh "./publish/{variant}"
"""


def deploy_sh(variant):
    return f"""#!/usr/bin/env bash
# {BANNER}
# Deploy the {variant} reference API to a remote VM.
#
# Usage:
#   ./deploy.sh <user@host> [--cert-dir /path/to/certs]
#
# Expects:
#   - ./publish/ directory (run build.sh first)
#   - Certificate files (cert.pem, key.pem) either in --cert-dir or /opt/bench/
#   - benchmarks/reference-apis/shared/bench-data.json at /opt/bench/ on the VM
#     (install.sh deploys it; the server refuses to start without it —
#     API-SPEC.md §2)
#
# BENCH_WORKERS (API-SPEC.md §3) is forwarded from the deployer's environment.
set -euo pipefail

SCRIPT_DIR="$(cd "$(dirname "$0")" && pwd)"
REMOTE_DIR="/opt/bench/{variant}"
CERT_DIR="/opt/bench"

if [[ $# -lt 1 ]]; then
    echo "Usage: $0 <user@host> [--cert-dir /path/to/certs]"
    exit 1
fi

TARGET="$1"
shift

while [[ $# -gt 0 ]]; do
    case "$1" in
        --cert-dir) CERT_DIR="$2"; shift 2 ;;
        *) echo "Unknown option: $1"; exit 1 ;;
    esac
done

if [[ ! -d "$SCRIPT_DIR/publish" ]]; then
    echo "Error: ./publish/ not found. Run build.sh first."
    exit 1
fi

echo "==> Deploying to $TARGET:$REMOTE_DIR"

# Create remote directory and stop any existing instance
ssh "$TARGET" "sudo mkdir -p $REMOTE_DIR && sudo mkdir -p $CERT_DIR && sudo pkill -f '{variant}' || true"

# Copy published binary
ssh "$TARGET" "mkdir -p /tmp/{variant}-deploy"
scp -r "$SCRIPT_DIR/publish/"* "$TARGET:/tmp/{variant}-deploy/"
ssh "$TARGET" "sudo mv /tmp/{variant}-deploy/* $REMOTE_DIR/ && rm -rf /tmp/{variant}-deploy"

# Copy certs if they exist locally
if [[ -f "$CERT_DIR/cert.pem" && -f "$CERT_DIR/key.pem" ]]; then
    scp "$CERT_DIR/cert.pem" "$CERT_DIR/key.pem" "$TARGET:/tmp/"
    ssh "$TARGET" "sudo mv /tmp/cert.pem /tmp/key.pem $CERT_DIR/"
fi

# Start server (BENCH_WORKERS is advisory for C#; recorded in the startup log)
ssh "$TARGET" "sudo chmod +x $REMOTE_DIR/{variant} && \\
    sudo BENCH_CERT_DIR=$CERT_DIR \\
         BENCH_PORT=8443 \\
         BENCH_WORKERS=${{BENCH_WORKERS:-}} \\
         nohup $REMOTE_DIR/{variant} > /var/log/{variant}.log 2>&1 &"

echo "==> Server started on $TARGET:8443"
echo "    Verify: curl -k https://$TARGET:8443/health"
"""


def readme(variant, tfm, aot):
    aot_line = "Native AOT (`PublishAot`)" if aot else "JIT (CoreCLR)"
    return f"""<!-- {BANNER} -->
# {variant} — C# reference API

One rung of the nine-variant C# runtime ladder (API-SPEC.md §8). All modern
variants share **one source file**: `../csharp-template/Program.cs` (this
directory's `Program.cs` is a generated byte-identical copy). Only the
runtime differs between variants.

| | |
|---|---|
| TargetFramework | `{tfm}` |
| Compilation | {aot_line} |
| Contract | `benchmarks/shared/API-SPEC.md` (frozen v1, family C) |
| /health runtime id | `{variant}` |

## Build & run

```bash
./build.sh                              # dotnet publish → ./publish/{variant}
docker build -t bench-{variant} .       # or the container image
```

The server needs `bench-data.json` (§2) — startup is fatal without it — and
serves HTTPS on 8443 when `$BENCH_CERT_DIR/cert.pem` + `key.pem` exist
(plain HTTP fallback otherwise, for the proxy-terminated topology). Validate:

```bash
../../validate/run-validation.sh --url=https://localhost:8443 --name={variant}
```

## Do not edit

Edit `../csharp-template/Program.cs` (or `generate-variants.py`) and rerun
`../csharp-template/generate-variants.py`; `--check` detects drift.
"""


GITIGNORE = f"""# {BANNER}
bin/
obj/
publish/
output/
"""


NET48_DEPLOY = f"""#!/usr/bin/env bash
# {BANNER}
# Deploy .NET Framework 4.8 benchmark server on Windows Server
# Called by install.sh --benchmark-server csharp-net48
set -euo pipefail

BENCH_DIR="${{BENCH_DIR:-/opt/bench}}"
CERT_DIR="${{BENCH_CERT_DIR:-$BENCH_DIR}}"
PORT="${{BENCH_PORT:-8443}}"

echo ">> Compiling .NET 4.8 server"
# csc.exe is at the .NET Framework path on Windows
CSC="/c/Windows/Microsoft.NET/Framework64/v4.0.30319/csc.exe"
if [ ! -f "$CSC" ]; then
    CSC="$(find /c/Windows/Microsoft.NET/Framework64 -name csc.exe 2>/dev/null | head -1)"
fi

"$CSC" /out:"$BENCH_DIR/csharp-net48.exe" \\
    /reference:System.IO.Compression.dll \\
    /reference:System.Web.Extensions.dll \\
    /target:exe \\
    "$(dirname "$0")/Server.cs" || {{
        echo "ERROR: csc.exe compilation failed"
        exit 1
    }}

echo ">> Setting up HTTPS binding"
# Import cert and bind to port
THUMBPRINT=$(powershell -Command "
    \\$cert = New-SelfSignedCertificate -DnsName 'bench' -CertStoreLocation Cert:\\LocalMachine\\My
    \\$cert.Thumbprint
" 2>/dev/null || echo "")

if [ -n "$THUMBPRINT" ]; then
    netsh http add urlacl url="https://+:${{PORT}}/" user=Everyone 2>/dev/null || true
    netsh http add sslcert ipport=0.0.0.0:${{PORT}} certhash="$THUMBPRINT" appid="{{12345678-1234-1234-1234-123456789012}}" 2>/dev/null || true
fi

echo ">> Starting .NET 4.8 server on port $PORT"
# The server exits non-zero when bench-data.json is absent (API-SPEC.md §2) —
# install.sh deploys it to /opt/bench/bench-data.json.
nohup "$BENCH_DIR/csharp-net48.exe" "$PORT" "$CERT_DIR" > /dev/null 2>&1 &
echo ">> csharp-net48 server started (PID $!)"
"""


NET48_DOCKERFILE = f"""# escape=`
# {BANNER}
# WINDOWS CONTAINERS ONLY — .NET Framework 4.8 has no Linux runtime. This
# Dockerfile builds on a Windows Docker host (`--platform windows/amd64`);
# the Linux validation compose intentionally excludes csharp-net48 and the
# variant is normally deployed to a Windows VM via deploy.sh instead.
FROM mcr.microsoft.com/dotnet/framework/sdk:4.8 AS build
WORKDIR C:\\src
COPY Server.cs .
RUN csc /out:C:\\src\\csharp-net48.exe `
    /reference:System.IO.Compression.dll `
    /reference:System.Web.Extensions.dll `
    /target:exe Server.cs

FROM mcr.microsoft.com/dotnet/framework/runtime:4.8
WORKDIR C:\\app
COPY --from=build C:\\src\\csharp-net48.exe .
ENV BENCH_CERT_DIR=C:\\opt\\bench
ENV BENCH_PORT=8443
EXPOSE 8443
# No netsh sslcert binding inside the container → the server falls back to
# plain HTTP (application-mode topology: a TLS-terminating proxy in front).
ENTRYPOINT ["C:\\\\app\\\\csharp-net48.exe"]
"""


NET48_README = f"""<!-- {BANNER} -->
# csharp-net48 — C# reference API (.NET Framework 4.8, Windows-only)

The one rung of the C# runtime ladder (API-SPEC.md §8) that cannot share the
modern template: .NET Framework has no Kestrel/minimal APIs, no
System.Text.Json, and no ZLibStream. Its template is
`../csharp-template/Server.cs` (this directory's `Server.cs` is a generated
byte-identical copy) — HttpListener, JavaScriptSerializer, C# 5.

| | |
|---|---|
| Runtime | .NET Framework 4.8 (Windows only, HTTP/1.1 only) |
| Contract | `benchmarks/shared/API-SPEC.md` (frozen v1, family C) |
| /health runtime id | `csharp-net48` |
| TLS | `netsh http add sslcert` binding (see deploy.sh); plain-HTTP fallback without certs |
| zlib caveat | RFC 1950 framing over .NET Framework's DeflateStream + Adler-32; `compressed_size` is tolerance-checked per §5.8 |

Deployment: `deploy.sh` (invoked by `install.sh --benchmark-server
csharp-net48` on a Windows VM). The Dockerfile requires **Windows
containers** and is excluded from the Linux validation compose.

## Do not edit

Edit `../csharp-template/Server.cs` (or `generate-variants.py`) and rerun
`../csharp-template/generate-variants.py`; `--check` detects drift.
"""


def emit(root):
    """Write all variant files under `root`; returns {relpath: executable}."""
    files = {}

    def put(variant, name, content, executable=False):
        path = os.path.join(root, variant, name)
        os.makedirs(os.path.dirname(path), exist_ok=True)
        with open(path, "w", encoding="utf-8", newline="\n") as fh:
            fh.write(content)
        if executable:
            mode = os.stat(path).st_mode
            os.chmod(path, mode | stat.S_IXUSR | stat.S_IXGRP | stat.S_IXOTH)
        files[os.path.join(variant, name)] = executable

    with open(os.path.join(SCRIPT_DIR, "Program.cs"), encoding="utf-8") as fh:
        program_cs = fh.read()
    with open(os.path.join(SCRIPT_DIR, "Server.cs"), encoding="utf-8") as fh:
        server_cs = fh.read()

    for variant, tfm, sdk, aot in MODERN:
        put(variant, "Program.cs", program_cs)
        put(variant, f"{variant}.csproj", csproj(variant, tfm, aot))
        put(variant, "Dockerfile", dockerfile(variant, sdk, aot))
        put(variant, "build.sh", build_sh(variant, aot), executable=True)
        put(variant, "deploy.sh", deploy_sh(variant), executable=True)
        put(variant, "README.md", readme(variant, tfm, aot))
        put(variant, ".gitignore", GITIGNORE)

    put("csharp-net48", "Server.cs", server_cs)
    put("csharp-net48", "deploy.sh", NET48_DEPLOY, executable=True)
    put("csharp-net48", "Dockerfile", NET48_DOCKERFILE)
    put("csharp-net48", "README.md", NET48_README)
    put("csharp-net48", ".gitignore", GITIGNORE)

    return files


def main():
    check = "--check" in sys.argv[1:]
    if not check:
        emit(REF_DIR)
        print("Regenerated 9 C# variants from csharp-template/.")
        return 0

    tmp = tempfile.mkdtemp(prefix="csharp-variants-")
    try:
        files = emit(tmp)
        drift = []
        for rel in sorted(files):
            expected_path = os.path.join(tmp, rel)
            actual_path = os.path.join(REF_DIR, rel)
            try:
                with open(actual_path, encoding="utf-8") as fh:
                    actual = fh.read()
            except OSError:
                drift.append(f"{rel}: missing")
                continue
            with open(expected_path, encoding="utf-8") as fh:
                expected = fh.read()
            if actual != expected:
                drift.append(f"{rel}: differs from the template output")
        if drift:
            for line in drift:
                print(f"DRIFT: {line}", file=sys.stderr)
            print("Run benchmarks/reference-apis/csharp-template/generate-variants.py "
                  "to regenerate.", file=sys.stderr)
            return 1
        print("OK: all 9 C# variants match the template.")
        return 0
    finally:
        shutil.rmtree(tmp, ignore_errors=True)


if __name__ == "__main__":
    sys.exit(main())
